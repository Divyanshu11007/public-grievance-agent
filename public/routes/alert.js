/* =========================================
   ALERT ROUTES
========================================= */

const express =
  require("express");

const router =
  express.Router();

/* =========================================
   MODELS
========================================= */

const Alert =
  require("../models/Alert");

const Complaint =
  require("../models/Complaint");

/* =========================================
   MIDDLEWARE
========================================= */

const auth =
  require("../middleware/auth");

const {
  roleAuth
} = require("../middleware/auth");

/* =========================================
   CREATE ALERT
========================================= */

router.post(
  "/",
  auth,
  roleAuth([
    "admin",
    "officer"
  ]),
  async (req, res) => {

    try {

      const {

        title,
        message,
        type,
        priority,
        complaintId,
        ward,
        lat,
        lng,
        targetRole

      } = req.body;

      /* =========================
         VALIDATION
      ========================= */

      if (!message) {

        return res.status(400).json({

          success: false,

          message:
            "Alert message required",

        });

      }

      /* =========================
         CREATE ALERT
      ========================= */

      const alert =
        new Alert({

          title:
            title || "System Alert",

          message,

          type:
            type || "Warning",

          priority:
            priority || "Medium",

          complaintId:
            complaintId || null,

          ward:
            ward || "Unknown",

          targetRole:
            targetRole || "all",

          location: {

            lat:
              lat || 0,

            lng:
              lng || 0,

          },

          autoGenerated:
            false,

        });

      await alert.save();

      res.status(201).json({

        success: true,

        message:
          "Alert created successfully",

        alert,

      });

    } catch (error) {

      console.log(error);

      res.status(500).json({

        success: false,

        message:
          error.message,

      });

    }

  }
);

/* =========================================
   GET ALL ALERTS
========================================= */

router.get(
  "/",
  auth,
  async (req, res) => {

    try {

      let alerts;

      /* =========================
         ROLE FILTER
      ========================= */

      if (
        req.user.role ===
        "citizen"
      ) {

        alerts =
          await Alert.find({

            $or: [

              {
                targetRole:
                  "citizen"
              },

              {
                targetRole:
                  "all"
              }

            ]

          }).sort({

            createdAt: -1

          });

      } else if (
        req.user.role ===
        "officer"
      ) {

        alerts =
          await Alert.find({

            $or: [

              {
                targetRole:
                  "officer"
              },

              {
                targetRole:
                  "all"
              }

            ]

          }).sort({

            createdAt: -1

          });

      } else {

        alerts =
          await Alert.find()
          .sort({

            createdAt: -1

          });

      }

      res.json({

        success: true,

        alerts,

      });

    } catch (error) {

      res.status(500).json({

        success: false,

        message:
          error.message,

      });

    }

  }
);

/* =========================================
   GET SINGLE ALERT
========================================= */

router.get(
  "/:id",
  auth,
  async (req, res) => {

    try {

      const alert =
        await Alert.findById(
          req.params.id
        ).populate(
          "complaintId"
        );

      if (!alert) {

        return res.status(404).json({

          success: false,

          message:
            "Alert not found",

        });

      }

      res.json({

        success: true,

        alert,

      });

    } catch (error) {

      res.status(500).json({

        success: false,

        message:
          error.message,

      });

    }

  }
);

/* =========================================
   MARK ALERT AS READ
========================================= */

router.put(
  "/:id/read",
  auth,
  async (req, res) => {

    try {

      const alert =
        await Alert.findById(
          req.params.id
        );

      if (!alert) {

        return res.status(404).json({

          success: false,

          message:
            "Alert not found",

        });

      }

      alert.read = true;

      await alert.save();

      res.json({

        success: true,

        message:
          "Alert marked as read",

        alert,

      });

    } catch (error) {

      res.status(500).json({

        success: false,

        message:
          error.message,

      });

    }

  }
);

/* =========================================
   RESOLVE ALERT
========================================= */

router.put(
  "/:id/resolve",
  auth,
  roleAuth([
    "admin",
    "officer"
  ]),
  async (req, res) => {

    try {

      const alert =
        await Alert.findById(
          req.params.id
        );

      if (!alert) {

        return res.status(404).json({

          success: false,

          message:
            "Alert not found",

        });

      }

      alert.resolved =
        true;

      alert.resolvedBy =
        req.user.id;

      alert.resolvedAt =
        new Date();

      await alert.save();

      res.json({

        success: true,

        message:
          "Alert resolved",

        alert,

      });

    } catch (error) {

      res.status(500).json({

        success: false,

        message:
          error.message,

      });

    }

  }
);

/* =========================================
   ESCALATE ALERT
========================================= */

router.put(
  "/:id/escalate",
  auth,
  roleAuth([
    "admin"
  ]),
  async (req, res) => {

    try {

      const alert =
        await Alert.findById(
          req.params.id
        );

      if (!alert) {

        return res.status(404).json({

          success: false,

          message:
            "Alert not found",

        });

      }

      alert.escalationLevel += 1;

      alert.priority =
        "Critical";

      await alert.save();

      res.json({

        success: true,

        message:
          "Alert escalated",

        alert,

      });

    } catch (error) {

      res.status(500).json({

        success: false,

        message:
          error.message,

      });

    }

  }
);

/* =========================================
   DELETE ALERT
========================================= */

router.delete(
  "/:id",
  auth,
  roleAuth([
    "admin"
  ]),
  async (req, res) => {

    try {

      const alert =
        await Alert.findByIdAndDelete(
          req.params.id
        );

      if (!alert) {

        return res.status(404).json({

          success: false,

          message:
            "Alert not found",

        });

      }

      res.json({

        success: true,

        message:
          "Alert deleted",

      });

    } catch (error) {

      res.status(500).json({

        success: false,

        message:
          error.message,

      });

    }

  }
);

/* =========================================
   ALERT STATS
========================================= */

router.get(
  "/stats/overview",
  auth,
  roleAuth([
    "admin",
    "officer"
  ]),
  async (req, res) => {

    try {

      const alerts =
        await Alert.find();

      const total =
        alerts.length;

      const resolved =
        alerts.filter(
          a => a.resolved
        ).length;

      const unread =
        alerts.filter(
          a => !a.read
        ).length;

      const critical =
        alerts.filter(
          a =>
            a.priority ===
            "Critical"
        ).length;

      res.json({

        success: true,

        stats: {

          total,
          resolved,
          unread,
          critical,

        }

      });

    } catch (error) {

      res.status(500).json({

        success: false,

        message:
          error.message,

      });

    }

  }
);

/* =========================================
   EXPORT ROUTER
========================================= */

module.exports =
  router;